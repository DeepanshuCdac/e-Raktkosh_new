	public static String getStatusText(String componentCount, String moderateCount) {
		// TODO Auto-generated method stub
		String status = "Not Available";
		if (componentCount != null) {
			int iComponentCount = Integer.parseInt(componentCount);
			int iModerateCount = getModerateCount(moderateCount);
			if (iComponentCount > 0 && iComponentCount <= iModerateCount) {
				status = "Moderate";
				// color = "text-warning";
			} else if (iComponentCount > iModerateCount) {
				status = "Available";
				// color = "text-success";
			}
		}
		return status;
	}

	public static String getColor(String status) {
		String color = "";
		if (status.equals("Moderate")) {
			color = "text-warning";
		} else if (status.equals("Available")) {
			color = "text-success";
		} else
			color = "text-danger"; // not available
		return color;
	}

	public static int getModerateCount(String moderateCount) {
		int count = 0;
		if (moderateCount != null && !moderateCount.isEmpty()) {
			count = Integer.parseInt(moderateCount);
		}

		return count;
	}

